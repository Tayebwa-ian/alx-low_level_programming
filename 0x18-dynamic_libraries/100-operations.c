/**
 *add - Add two numbers
 *@a: first number
 *@b: second number
 *Return: resulting number
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 *sub - Subtract two numbers
 *@a: first number
 *@b: second number
 *Return: resulting number
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 *mul - multiply two numbers
 *@a: first number
 *@b: second number
 *Return: resulting number
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 *div - divide two numbers
 *@a: first number
 *@b: second number
 *Return: resulting number
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 *mod - Module of two numbers
 *@a: first number
 *@b: second number
 *Return: resulting number
 */
int mod(int a, int b)
{
	return (a % b);
}
